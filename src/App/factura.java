    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       // Obtener el contenido del JTextArea
    String texto = txtbill.getText();

    // Crear una instancia de PrinterJob
    PrinterJob printerJob = PrinterJob.getPrinterJob();

    // Asignar la clase que implementa Printable
    printerJob.setPrintable(new Printable() {
        @Override
        public int print(Graphics g, PageFormat pageFormat, int pageIndex) throws PrinterException {
            if (pageIndex > 0) {
                return Printable.NO_SUCH_PAGE;
            }

            // Configurar la posición y tamaño del área imprimible
            txtbill.setSize((int) pageFormat.getImageableWidth(), (int) pageFormat.getImageableHeight());
            txtbill.print(g);

            return Printable.PAGE_EXISTS;
        }
    });

    // Mostrar el diálogo de impresión
    if (printerJob.printDialog()) {
        try {
            // Iniciar el trabajo de impresión
            printerJob.print();
        } catch (PrinterException ex) {
            ex.printStackTrace();
        }
    }
    }//GEN-LAST:event_jButton1ActionPerformed

